def identify_contract_type():
    print("=== ОПРЕДЕЛЕНИЕ ВИДА ДОГОВОРА ===\n")

    print("Ответьте на вопросы о сделке:\n")

    # Вопрос 1
    payment_type = input("1. Деньги передаются в собственность? (да/нет): ").lower()

    if payment_type == 'да':
        # Вопрос 2
        exchange_goods = input("2. Обмен денег на товар? (да/нет): ").lower()

        if exchange_goods == 'да':
            print("\n✓ ДОГОВОР КУПЛИ-ПРОДАЖИ")
            print("Передача товара в собственность за денежное вознаграждение")

        else:
            # Вопрос 3
            temporary_use = input("3. Передача имущества во временное пользование? (да/нет): ").lower()

            if temporary_use == 'да':
                print("\n✓ ДОГОВОР АРЕНДЫ")
                print("Передача имущества во временное владение и пользование")

            else:
                print("\n✓ ДОГОВОР ЗАЙМА")
                print("Передача денег в собственность с обязательством возврата")

    else:
        # Вопрос 4
        perform_work = input("4. Выполнение конкретной работы? (да/нет): ").lower()

        if perform_work == 'да':
            print("\n✓ ДОГОВОР ПОДРЯДА")
            print("Выполнение работы с передачей результата")

        else:
            # Вопрос 5
            provide_services = input("5. Оказание услуг? (да/нет): ").lower()

            if provide_services == 'да':
                print("\n✓ ДОГОВОР ВОЗМЕЗДНОГО ОКАЗАНИЯ УСЛУГ")
                print("Оказание услуг без материального результата")

            else:
                print("\n✓ ДОГОВОР БЕЗВОЗМЕЗДНОГО ПОЛЬЗОВАНИЯ")
                print("Безвозмездная передача в пользование")


# Запуск
identify_contract_type()