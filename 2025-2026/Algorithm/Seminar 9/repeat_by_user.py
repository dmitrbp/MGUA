# Выполнение действия с возможностью повтора
print("Добро пожаловать!")
while True:
    # Тело цикла выполнится хотя бы один раз
    name = input("Введите ваше имя: ")
    print(f"Привет, {name}!")

    # Проверка условия продолжения после выполнения
    answer = input("Повторить? (да/нет): ").lower()
    if answer != 'да':
        break  # Выход если пользователь не хочет продолжать

print("До свидания!")