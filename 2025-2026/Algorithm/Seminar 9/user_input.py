# Классический паттерн "первая инициализация"
total = 0
count = 0

# Первое чтение перед циклом
value = float(input("Введите число (0 для окончания): "))

while value != 0:  # Условие продолжения
    total += value
    count += 1
    # Обновление значения для следующей проверки условия
    value = float(input("Введите следующее число (0 для окончания): "))

if count > 0:
    print(f"Среднее: {total / count}")
else:
    print("Не введено ни одного числа")