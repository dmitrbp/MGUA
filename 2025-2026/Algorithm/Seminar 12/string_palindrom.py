# Проверка, является ли строка палиндромом
def is_palindrome(s):
    """Проверяет, читается ли строка одинаково слева направо и справа налево"""
    s = s.lower().replace(' ', '')  # Удаляем пробелы и приводим к нижнему регистру
    return s == s[::-1]

words = ["заказ", "показ", "довод", "провод", "кабак", "казак", "рассказ", "комок"]
for word in words:
    print(f'word - {word}, is polindrom - {is_palindrome(word)}')