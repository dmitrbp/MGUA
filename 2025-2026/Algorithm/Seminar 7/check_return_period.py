def check_return_period():
    print("=== АЛГОРИТМ ПРОВЕРКИ ВОЗВРАТА ТОВАРА ===\n")

    # Ветвление 1: Качество товара
    is_good_quality = input("Товар надлежащего качества? (да/нет): ").lower().strip() == 'да'

    if not is_good_quality:
        print("✓ Возврат возможен в течение всего срока гарантии или 2 лет (если гарантия не установлена)")
        return "Возврат возможен (ненадлежащее качество)"

    # Ветвление 2: Срок с момента покупки
    try:
        days_passed = int(input("Сколько дней прошло с момента покупки? "))
    except ValueError:
        print("✗ Ошибка: введите число дней")
        return "Ошибка ввода"

    if days_passed > 14:
        # Ветвление 3: Тип товара
        is_food = input("Товар продовольственный? (да/нет): ").lower().strip() == 'да'

        if is_food:
            print("✗ Возврат невозможен: продовольственные товары надлежащего качества возврату не подлежат")
            return "Возврат невозможен"
        else:
            # Ветвление 4: Состояние товара
            has_preserved_look = input("Сохранился товарный вид, ярлыки, пломбы? (да/нет): ").lower().strip() == 'да'
            has_receipt = input("Сохранился чек или иное подтверждение покупки? (да/нет): ").lower().strip() == 'да'

            if has_preserved_look and has_receipt:
                print("✓ Возврат возможен (прошло более 14 дней, но сохранены все условия)")
                return "Возврат возможен"
            else:
                print("✗ Возврат невозможен: не сохранены товарный вид или подтверждение покупки")
                return "Возврат невозможен"
    else:
        print("✓ Возврат возможен: прошло менее 15 дней")
        return "Возврат возможен"


# Запуск алгоритма
result = check_return_period()
print(f"\nРезультат: {result}")