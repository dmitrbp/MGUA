a = 22
b = 4

print(f'{a} + {b} = {a + b}')    # сложение
print(f'{a} - {b} = {a - b}')    # вычитание
print(f'{a} * {b} = {a * b}')    # умножение
print(f'{a} / {b} = {a / b}')    # деление с остатком
print(f'{a} // {b} = {a // b}')  # деление без остатка
print(f'{a} % {b} = {a % b}')    # остаток от деления
